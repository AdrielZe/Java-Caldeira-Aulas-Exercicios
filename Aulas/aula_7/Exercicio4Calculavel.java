package aula_7;

public interface Exercicio4Calculavel {
    public static int sum(int n1,int n2) {
        return (n1+n2);
    }
    public static int subtract(int n1,int n2){
        return (n1-n2);
    }
    public static int multiply(int n1,int n2){
        return (n1*n2);
    }
    public static double divide(int n1, int n2){
        return (double)(n1/n2);
    }
}
